    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        XYSeries oSeries = new XYSeries("Humedad");
        
        int dato1 = Integer.parseInt(txtDato1.getText());
        int dato2 = Integer.parseInt(txtDato2.getText());
        int dato3 = Integer.parseInt(txtDato3.getText());
        int dato4 = Integer.parseInt(txtDato4.getText());
          
        oSeries.add(1, dato1);
        oSeries.add(2, dato2);
        oSeries.add(3, dato3);
        oSeries.add(4, dato4);
        
        XYSeriesCollection oDataset = new XYSeriesCollection();
        oDataset.addSeries(oSeries);
        
        JFreeChart oChart = ChartFactory.createXYLineChart("Graficos del sistema Vivero: Humedad", "Dias", "Datos en L/S", oDataset, PlotOrientation.VERTICAL, true, true, true);
        
        ChartPanel oPanel = new ChartPanel(oChart);
        pnlMain.setLayout(new java.awt.BorderLayout());
        pnlMain.add(oPanel);
        pnlMain.validate();
        
        XYSeries oSeries2 = new XYSeries("Temperatura");
        
        int dato5 = Integer.parseInt(txtDato5.getText());
        int dato6 = Integer.parseInt(txtDato6.getText());
        int dato7 = Integer.parseInt(txtDato7.getText());
        int dato8 = Integer.parseInt(txtDato8.getText());
        
        oSeries2.add(1, dato5);
        oSeries2.add(2, dato6);
        oSeries2.add(3, dato7);
        oSeries2.add(4, dato8);
        
        XYSeriesCollection oDataset2 = new XYSeriesCollection();
        oDataset2.addSeries(oSeries2);
        
        JFreeChart oChart2 = ChartFactory.createXYLineChart("Graficos del sistema Vivero: Temperatura", "Dias", "Datos en ¬∞C", oDataset2, PlotOrientation.VERTICAL, true, false, false);
        
        ChartPanel oPanel2 = new ChartPanel(oChart2);
        pnlMain2.setLayout(new java.awt.BorderLayout());
        pnlMain2.add(oPanel2);
        pnlMain2.validate();
        
    }//GEN-LAST:event_jButton1ActionPerformed

